static const uint8_t* font5x7(char c) {
    if (c < 32 || c > 122) return &basic5x7[0]; 
    return &basic5x7[(c - 32) * 5];
}

void B4RAda_NeoPixel::ScrollText(B4R::B4RString* Text, uint32_t Colour, uint16_t SpeedMs) {
    if (!strip || !Text) return;
    const char* ctext = Text->data;
    if (!ctext) return;

    uint16_t numPixels = strip->numPixels();
    uint16_t mWidth = 8; 
    uint16_t mHeight = 8;

    int textLen = strlen(ctext);
    int totalCols = textLen * 6; 

    for (int offset = 0; offset < totalCols + mWidth; offset++) {
        strip->clear();
        for (int x = 0; x < mWidth; x++) {
            int colIdx = offset - (mWidth - 1) + x;
            if (colIdx >= 0 && colIdx < totalCols) {
                int charIdx = colIdx / 6;
                int subCol = colIdx % 6;
                if (subCol < 5) {
                    char c = ctext[charIdx];
                    uint8_t bits = font5x7(c)[subCol];
                    for (int y = 0; y < 8; y++) {
                        if (bits & (1 << y)) {
                            strip->setPixelColor(y * mWidth + x, Colour);
                        }
                    }
                }
            }
        }
        strip->show();
        delay(SpeedMs);
    }
}
