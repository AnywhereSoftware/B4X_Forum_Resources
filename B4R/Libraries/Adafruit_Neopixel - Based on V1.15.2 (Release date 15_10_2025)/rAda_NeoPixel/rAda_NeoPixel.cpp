void B4RAda_NeoPixel::MovePixel(uint16_t From, uint16_t To) {
    if (!strip) return;
    if (From >= strip->numPixels() || To >= strip->numPixels()) return;

    uint32_t colour = strip->getPixelColor(From);
    strip->setPixelColor(From, 0);
    strip->setPixelColor(To, colour);
}

void B4RAda_NeoPixel::AnimatePixelColor(uint16_t Start, uint16_t Finish, uint32_t Colour, uint16_t DelayMs) {
    if (!strip) return;

    uint16_t count = strip->numPixels();
    if (Start >= count || Finish >= count) return;

    int step = (Start <= Finish) ? 1 : -1;

    for (int i = Start; i != (int)Finish; i += step) {
        strip->setPixelColor(i, Colour);
        strip->show();
        delay(DelayMs);

        strip->setPixelColor(i, 0);
        strip->show();

        delay(1);
    }

    strip->setPixelColor(Finish, Colour);
    strip->show();
}

uint32_t B4RAda_NeoPixel::SetRGB(uint8_t Red, uint8_t Green, uint8_t Blue) {
    return ((uint32_t)Red << 16) | ((uint32_t)Green << 8) | Blue;
}

uint32_t B4RAda_NeoPixel::SetRGBW(uint8_t Red, uint8_t Green, uint8_t Blue, uint8_t White) {
    return ((uint32_t)White << 24) | ((uint32_t)Red << 16) | ((uint32_t)Green << 8) | Blue;
}

uint32_t B4RAda_NeoPixel::DimPixel(uint32_t Colour, uint8_t Percent) {
    if (Percent >= 100) return Colour;
    if (Percent == 0) return 0;

    uint8_t r = (Colour >> 16) & 0xFF;
    uint8_t g = (Colour >> 8) & 0xFF;
    uint8_t b = Colour & 0xFF;
    uint8_t w = (Colour >> 24) & 0xFF;

    uint16_t p = Percent;
    uint8_t nr = (uint8_t)((r * p) / 100);
    uint8_t ng = (uint8_t)((g * p) / 100);
    uint8_t nb = (uint8_t)((b * p) / 100);
    uint8_t nw = (uint8_t)((w * p) / 100);

    return ((uint32_t)nw << 24) | ((uint32_t)nr << 16) | ((uint32_t)ng << 8) | nb;
}

void B4RAda_NeoPixel::FadePixel(uint16_t Index, uint32_t Colour, uint16_t Steps, uint16_t DelayMs) {
    if (!strip) return;
    if (Index >= strip->numPixels()) return;
    if (Steps == 0) Steps = 1;

    uint32_t start = strip->getPixelColor(Index);

    uint8_t sr = (start >> 16) & 0xFF;
    uint8_t sg = (start >> 8) & 0xFF;
    uint8_t sb = start & 0xFF;
    uint8_t sw = (start >> 24) & 0xFF;

    uint8_t tr = (Colour >> 16) & 0xFF;
    uint8_t tg = (Colour >> 8) & 0xFF;
    uint8_t tb = Colour & 0xFF;
    uint8_t tw = (Colour >> 24) & 0xFF;

    for (uint16_t step = 1; step <= Steps; ++step) {
        uint8_t nr = sr + (int)((int)tr - (int)sr) * step / Steps;
        uint8_t ng = sg + (int)((int)tg - (int)sg) * step / Steps;
        uint8_t nb = sb + (int)((int)tb - (int)sb) * step / Steps;
        uint8_t nw = sw + (int)((int)tw - (int)sw) * step / Steps;

        uint32_t packed = ((uint32_t)nw << 24) | ((uint32_t)nr << 16) | ((uint32_t)ng << 8) | nb;
        strip->setPixelColor(Index, packed);
        strip->show();
        delay(DelayMs);
    }
}

